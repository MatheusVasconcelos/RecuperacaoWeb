package Objetos;

public class Fatura {
	private int cpf;
	private String nome;
	private String tipo;
	private int lugar;
	
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public int getLugar() {
		return lugar;
	}
	public void setLugar(int lugar) {
		this.lugar = lugar;
	}
	
}
